/*
Given a grid cell and an isolevel, calculate the triangular
facets required to represent the isosurface through the cell.
Return the number of triangular facets, the array "triangles"
will be loaded up with the vertices at most 5 triangular facets.
0 will be returned if the grid cell is either totally above
or totally below the isolevel.
*/
int Polygonise(MC_Gridcell grid, float isolevel, MC_Triangle* triangles) {

	int ntriang;
	int cubeindex;
	Vector3f vertlist[12];
	Vector4uc vertColor[12];

	cubeindex = 0;
	if (grid.val[0] < isolevel) cubeindex |= 1;
	if (grid.val[1] < isolevel) cubeindex |= 2;
	if (grid.val[2] < isolevel) cubeindex |= 4;
	if (grid.val[3] < isolevel) cubeindex |= 8;
	if (grid.val[4] < isolevel) cubeindex |= 16;
	if (grid.val[5] < isolevel) cubeindex |= 32;
	if (grid.val[6] < isolevel) cubeindex |= 64;
	if (grid.val[7] < isolevel) cubeindex |= 128;

	/* Cube is entirely in/out of the surface */
	if (edgeTable[cubeindex] == 0)
		return 0;

	if (
		grid.val[0] == grid.val[1] ||
		grid.val[1] == grid.val[2] ||
		grid.val[2] == grid.val[3] ||
		grid.val[3] == grid.val[0] ||
		grid.val[4] == grid.val[5] ||
		grid.val[5] == grid.val[6] ||
		grid.val[6] == grid.val[7] ||
		grid.val[7] == grid.val[4] ||
		grid.val[0] == grid.val[4] ||
		grid.val[1] == grid.val[5] ||
		grid.val[2] == grid.val[6] ||
		grid.val[3] == grid.val[7]
		)
		return 0;

	//for (int i = 0; i < 8; i++)
	//	std::cout << grid.p[i] << std::endl << grid.val[i] << std::endl << grid.color[i] << std::endl;

	/* Find the vertices where the surface intersects the cube */
	if (edgeTable[cubeindex] & 1) {
		vertlist[0] = VertexInterp(isolevel, grid.p[0], grid.p[1], grid.val[0], grid.val[1]);
		vertColor[0] = ColorInterp(isolevel, grid.color[0], grid.color[1], grid.val[0], grid.val[1]);
	}
	if (edgeTable[cubeindex] & 2) {
		vertlist[1] = VertexInterp(isolevel, grid.p[1], grid.p[2], grid.val[1], grid.val[2]);
		vertColor[1] = ColorInterp(isolevel, grid.color[1], grid.color[2], grid.val[1], grid.val[2]);
	}
	if (edgeTable[cubeindex] & 4) {
		vertlist[2] = VertexInterp(isolevel, grid.p[2], grid.p[3], grid.val[2], grid.val[3]);
		vertColor[2] = ColorInterp(isolevel, grid.color[2], grid.color[3], grid.val[2], grid.val[3]);
	}
	if (edgeTable[cubeindex] & 8) {
		vertlist[3] = VertexInterp(isolevel, grid.p[3], grid.p[0], grid.val[3], grid.val[0]);
		vertColor[3] = ColorInterp(isolevel, grid.color[3], grid.color[0], grid.val[3], grid.val[0]);
	}
	if (edgeTable[cubeindex] & 16) {
		vertlist[4] = VertexInterp(isolevel, grid.p[4], grid.p[5], grid.val[4], grid.val[5]);
		vertColor[4] = ColorInterp(isolevel, grid.color[4], grid.color[5], grid.val[4], grid.val[5]);
	}
	if (edgeTable[cubeindex] & 32) {
		vertlist[5] = VertexInterp(isolevel, grid.p[5], grid.p[6], grid.val[5], grid.val[6]);
		vertColor[5] = ColorInterp(isolevel, grid.color[5], grid.color[6], grid.val[5], grid.val[6]);
	}
	if (edgeTable[cubeindex] & 64) {
		vertlist[6] = VertexInterp(isolevel, grid.p[6], grid.p[7], grid.val[6], grid.val[7]);
		vertColor[6] = ColorInterp(isolevel, grid.color[6], grid.color[7], grid.val[6], grid.val[7]);
	}
	if (edgeTable[cubeindex] & 128) {
		vertlist[7] = VertexInterp(isolevel, grid.p[7], grid.p[4], grid.val[7], grid.val[4]);
		vertColor[7] = ColorInterp(isolevel, grid.color[7], grid.color[4], grid.val[7], grid.val[4]);
	}
	if (edgeTable[cubeindex] & 256) {
		vertlist[8] = VertexInterp(isolevel, grid.p[0], grid.p[4], grid.val[0], grid.val[4]);
		vertColor[8] = ColorInterp(isolevel, grid.color[0], grid.color[4], grid.val[0], grid.val[4]);
	}
	if (edgeTable[cubeindex] & 512) {
		vertlist[9] = VertexInterp(isolevel, grid.p[1], grid.p[5], grid.val[1], grid.val[5]);
		vertColor[9] = ColorInterp(isolevel, grid.color[1], grid.color[5], grid.val[1], grid.val[5]);
	}
	if (edgeTable[cubeindex] & 1024) {
		vertlist[10] = VertexInterp(isolevel, grid.p[2], grid.p[6], grid.val[2], grid.val[6]);
		vertColor[10] = ColorInterp(isolevel, grid.color[2], grid.color[6], grid.val[2], grid.val[6]);
	}
	if (edgeTable[cubeindex] & 2048) {
		vertlist[11] = VertexInterp(isolevel, grid.p[3], grid.p[7], grid.val[3], grid.val[7]);
		vertColor[11] = ColorInterp(isolevel, grid.color[3], grid.color[7], grid.val[3], grid.val[7]);
	}

	//for (int i = 0; i < 12; i++)
	//	std::cout << vertlist[i] << std::endl;

	/* Create the triangle */
	ntriang = 0;
	for (int i = 0; triTable[cubeindex][i] != -1; i += 3) {
		triangles[ntriang].p[0] = vertlist[triTable[cubeindex][i]];
		triangles[ntriang].color[0] = vertColor[triTable[cubeindex][i]];

		triangles[ntriang].p[1] = vertlist[triTable[cubeindex][i + 1]];
		triangles[ntriang].color[1] = vertColor[triTable[cubeindex][i + 1]];

		triangles[ntriang].p[2] = vertlist[triTable[cubeindex][i + 2]];
		triangles[ntriang].color[2] = vertColor[triTable[cubeindex][i + 2]];

		ntriang++;
	}

	return ntriang;
}


